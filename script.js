function openModal(prog) {
  modalTitle.textContent = prog.name;
  modalTabs.innerHTML = `<button class="modal-tab active" data-tab="overview">نظرة عامة</button><button class="modal-tab" data-tab="jobs">فرص العمل</button><button class="modal-tab" data-tab="plan">الخطة الدراسية</button>`;
  let planHtml = "<p>لم تدرج بعد</p>";
  if (prog.plan && prog.plan.length) {
    planHtml = `<div class="plan-table-container"><table class="plan-table"><thead><tr><th>الفصل الدراسي</th><th>رمز المساق</th><th>اسم المساق</th></tr></thead><tbody>`;
    prog.plan.forEach((sem) => {
      const semName = sem[0];
      sem.slice(1).forEach((course) => {
        const match = course.match(/^([A-Z0-9]+\s\d+)\s(.*)$/);
        if (match) {
          planHtml += `<tr><td>${semName}</td><td><strong>${match[1]}</strong></td><td>${match[2]}</td></tr>`;
        } else {
          planHtml += `<tr><td>${semName}</td><td colspan="2">${course}</td></tr>`;
        }
      });
    });
    planHtml += `</tbody></table></div>`;
  }
  modalBody.innerHTML = `
        <div class="tab-pane active" id="tab-overview"><p>${prog.desc}</p><p><strong>الدرجة:</strong> ${prog.degree} | <strong>الساعات:</strong> ${prog.hours}</p></div>
        <div class="tab-pane" id="tab-jobs"><p>${prog.opportunities || "قريباً"}</p></div>
        <div class="tab-pane" id="tab-plan">${planHtml}</div>
    `;
  modal.classList.add("show");
}
modalClose.addEventListener("click", () => modal.classList.remove("show"));
modal.addEventListener("click", (e) => {
  if (e.target === modal) modal.classList.remove("show");
});

