function renderPrograms() {
  grid.innerHTML = "";
  let filtered = programs;
  if (currentFilter !== "all")
    filtered = filtered.filter((p) => p.dept === currentFilter);
  if (currentDegree !== "all")
    filtered = filtered.filter((p) => p.degree === currentDegree);
  if (currentSearch.trim()) {
    const q = currentSearch.toLowerCase();
    filtered = filtered.filter((p) => p.name.includes(q) || p.desc.includes(q));
  }
  if (!filtered.length) {
    grid.innerHTML =
      '<p style="text-align:center;grid-column:1/-1;color:var(--text-muted);">لا توجد نتائج.</p>';
    return;
  }
  filtered.forEach((p) => {
    const badgeClass = p.degree === "بكالوريوس" ? "badge-bs" : "badge-dip";
    const card = document.createElement("div");
    card.className = "prog-card";
    card.innerHTML = `<i class="fas ${p.icon} icon"></i><h3>${p.name}</h3><p style="color:var(--text-muted);font-size:0.9rem;">${p.desc}</p>`;
    if (p.links && p.links.length) {
      const linksDiv = document.createElement("div");
      linksDiv.className = "prog-links";
      p.links.forEach(
        (l) =>
          (linksDiv.innerHTML += `<a href="${l.url}" target="_blank" class="prog-link">${l.label}</a>`),
      );
      card.appendChild(linksDiv);
    }
    const meta = document.createElement("div");
    meta.className = "meta";
    meta.innerHTML = `<span><i class="fas fa-clock"></i> ${p.hours} ساعة</span><span class="badge ${badgeClass}">${p.degree}</span>`;
    card.appendChild(meta);
    card.addEventListener("click", (e) => {
      if (!e.target.closest("a")) openModal(p);
    });
    grid.appendChild(card);
  });
}

